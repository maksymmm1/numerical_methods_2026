import numpy as np


def rosenbrock(x):
    """
    Функція Розенброка (banana function).
    f(x1, x2) = 100*(x2 - x1^2)^2 + (1 - x1)^2
    Мінімум: f(1, 1) = 0
    """
    x1, x2 = x[0], x[1]
    return 100.0 * (x2 - x1**2)**2 + (1.0 - x1)**2


def power_function(x):
    """
    Степенева функція.
    f(x1, x2) = (x1^2 + x2 - 11)^2 + (x1 + x2^2 - 7)^2
    Мінімуми в точках (3,2), (-2.805,3.131), (-3.779,-3.283), (3.584,-1.848)
    """
    x1, x2 = x[0], x[1]
    return (x1**2 + x2 - 11)**2 + (x1 + x2**2 - 7)**2


def root_function(x):
    """
    Коренева функція (функція Бута).
    f(x1, x2) = (x1 + 2*x2 - 7)^2 + (2*x1 + x2 - 5)^2
    Мінімум: f(1, 3) = 0
    """
    x1, x2 = x[0], x[1]
    return (x1 + 2*x2 - 7)**2 + (2*x1 + x2 - 5)**2


def wood(x):
    """
    Функція Вуда (4 змінні).
    Мінімум: f(1,1,1,1) = 0
    """
    x1, x2, x3, x4 = x[0], x[1], x[2], x[3]
    return (100*(x2 - x1**2)**2 + (1 - x1)**2 +
            90*(x4 - x3**2)**2 + (1 - x3)**2 +
            10*(x2 + x4 - 2)**2 + 0.1*(x2 - x4)**2)


def powell(x):
    """
    Функція Пауелла (4 змінні).
    Мінімум: f(0,0,0,0) = 0
    """
    x1, x2, x3, x4 = x[0], x[1], x[2], x[3]
    return ((x1 + 10*x2)**2 + 5*(x3 - x4)**2 +
            (x2 - 2*x3)**4 + 10*(x1 - x4)**4)


def miele(x):
    """
    Функція Мієлє (4 змінні).
    Мінімум: f(0,1,1,1) = 0
    """
    x1, x2, x3, x4 = x[0], x[1], x[2], x[3]
    return ((np.exp(x1) - x2)**4 + 100*(x2 - x3)**6 +
            np.tan(x3 - x4)**4 + x1**8)


