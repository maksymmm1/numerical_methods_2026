"""
Система нелінійних рівнянь та побудова цільової функції.
Лабораторна робота №9, Крок 1 та 4.

Система (m=2):
  f1(x1, x2) = x1^2 + x2^2 - 4 = 0   (коло радіуса 2)
  f2(x1, x2) = x1 * x2 - 1     = 0   (гіпербола)

Аналітичний розв'язок:
  x1^2 + 1/x1^2 = 4  =>  x1^4 - 4*x1^2 + 1 = 0
  x1^2 = 2 ± sqrt(3)  =>  є 4 дійсних розв'язки
"""

import numpy as np

_a = np.sqrt(2 + np.sqrt(3))   # ≈ 1.9319
_b = 1.0 / _a                  # ≈ 0.5176

def f1(x):
    """Рівняння 1: x1^2 + x2^2 - 4 = 0"""
    return x[0]**2 + x[1]**2 - 4.0

def f2(x):
    """Рівняння 2: x1 * x2 - 1 = 0"""
    return x[0] * x[1] - 1.0

def system_equations(x):
    """Повертає вектор значень системи рівнянь."""
    return np.array([f1(x), f2(x)])

def objective_function(x):
    """
    Цільова функція F(x) = f1(x)^2 + f2(x)^2.
    Мінімум = 0 в точках розв'язку системи.
    """
    return f1(x)**2 + f2(x)**2


# Усі 4 точні розв'язки системи
EXACT_SOLUTIONS = [
    np.array([ _a,  _b]),
    np.array([ _b,  _a]),
    np.array([-_a, -_b]),
    np.array([-_b, -_a]),
]

# Початкові наближення для пошуку різних розв'язків
