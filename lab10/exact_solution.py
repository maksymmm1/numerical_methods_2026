# exact_solution.py
# ============================================================
# Аналітичний розв'язок задачі Коші
# ДР: y' = f(x, y),  y(x0) = y0,  x in [x0, x_end]
#
# Варіант (з методички):
#   y' = f(x, y)  — підставте своє рівняння
#   Для демонстрації використовуємо: y' = -2y + x,  y(0) = 1
#   Аналітичний розв'язок: y(x) = (x/2 - 1/4) + (5/4)*exp(-2x)
#
# Щоб змінити рівняння — відредагуйте функції f(), y_exact()
# та початкові умови X0, Y0, X_END, H нижче.
# ============================================================

import numpy as np

# ----------------------------------------------------------
# Параметри задачі (змінюйте під свій варіант)
# ----------------------------------------------------------
X0    = 0.0      # початкова точка
Y0    = 1.0      # початкова умова y(x0) = y0
X_END = 1.0      # кінець відрізку
H     = 0.1      # крок сітки
EPS   = 1e-4     # задана точність


def f(x, y):
    """
    Права частина ДР першого порядку: y' = f(x, y)
    Рівняння: y' = -2*y + x
    """
    return -2.0 * y + x


def y_exact(x):
    """
    Точний (аналітичний) розв'язок задачі Коші.
    Для y' = -2y + x, y(0) = 1:
        y(x) = x/2 - 1/4 + (5/4)*exp(-2x)
    """
    return x / 2.0 - 1.0 / 4.0 + (5.0 / 4.0) * np.exp(-2.0 * x)


def get_exact_grid(x0=X0, x_end=X_END, h=H):
    """
    Повертає масиви вузлів та точних значень розв'язку.
    """
    xs = np.arange(x0, x_end + h / 2.0, h)
    ys = y_exact(xs)
    return xs, ys


if __name__ == "__main__":
    xs, ys = get_exact_grid()
    print("Точний розв'язок на рівномірній сітці:")
    print(f"{'x':>10}  {'y_exact':>15}")
    print("-" * 30)
    for x, y in zip(xs, ys):
        print(f"{x:>10.4f}  {y:>15.8f}")